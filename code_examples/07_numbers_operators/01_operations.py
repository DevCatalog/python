# Examples of the four main math operations
print("Addition: 5 + 2 = ", 5 + 2)
print("Subtraction: 5 - 2 = ", 5 - 2)
print("Multiplication: 5 * 2 = ", 5 * 2)
print("Division: 5 / 2 = ", 5 / 2)

#  Examples of other operators
print("Modulus: 5 % 2 = ", 5 % 2)
print("Exponentiation: 5 ** 2 = ", 5 ** 2)
print("Floor Division: 5 // 2 = ", 5 // 2)

# Checking if a number is even or odd
print(6 % 2, "which is even")
print(7 % 2, "which is odd")

# Example of operator precedence using PEMDAS
print("Result=", (5 + 3) * 4 ** 2 / 8 - 1)  # Result= 15
