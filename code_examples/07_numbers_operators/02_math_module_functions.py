import math

# 1. fabs(x)
print(math.fabs(-2))

# 2. ceil(x)
print(math.ceil(5.3))

# 3. floor(x)
print(math.floor(4.8))

# 4. pow(x)
print(math.pow(2, 3))

# 5. sqrt(x)
print(math.sqrt(16))

# 6. Variables
print("e = ", math.e)
print("pi = ", math.pi)

# 7. Logarithm
print(math.log(math.e))

# 8. Exponential
print(math.exp(3))

# 9. Trigonometric Functions
print("Sine = ", math.sin(math.pi / 2))
print("Cosine = ", math.cos(math.pi))
print("Tangent = ", math.tan(math.pi / 4))

# 10. Converts the angle
print("Degrees = ", math.degrees(math.pi / 2))
print("Radians = ", math.radians(180))
