# Descriptive names
user_age = 20  # Better than ‘age’

# Case sensitive
variable = "value"
Variable = "value"
VARIABLE = "value"

# Lowercase letters with underscores
my_variable = "something"  # known as 'snake_case'
calculate_total_cost = 0

# Start with a letter or underscore
# 1sum = 20  # Wrong
_1sum = 20  # Correct
sum1 = 20  # Correct

# No special characters
# discount_% = 20  # Wrong
discount_percentage = 20  # Correct

# Avoid Reserved Words
# for = 15  # Wrong
_for = 15  # Correct
