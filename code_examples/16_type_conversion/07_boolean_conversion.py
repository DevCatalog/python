# Converting to True (Truthy values)
print("3.14 →", bool(3.14))   # Output: True
print("Python →", bool("Python"))   # Output: True

# Converting to False (Falsy values)
print("0 →", bool(0))   # Output: False
print("\"\" →", bool(""))   # Output: False
print("None →", bool(None))   # Output: False
